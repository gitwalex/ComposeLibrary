val LocalExtendedColors = staticCompositionLocalOf { ExtendedColorScheme(material = lightScheme) }


/**
 * AppTheme mit CustomColors
 * @see <a href="https://gustav-karlsson.medium.com/extending-the-jetpack-compose-material-theme-with-more-colors-e1b849390d50"></a>
 * @see <a href="https://gist.github.com/Khazbs/1f1f1b5c05f45dbfa465f249b1e20506"></a>
 */
@Composable
fun GerwalexTheme(
    isSystemInDarkMode: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
//    val dataStore = rememberDataStore()
    var useDarkTheme by remember { mutableStateOf(isSystemInDarkMode) }
//    LaunchedEffect(dataStore) {
//        dataStore.data.collectLatest {
//            useDarkTheme = it.useDarkMode || isSystemInDarkMode
//        }
//    }
    val view = LocalView.current
    AnimatedContent(
        targetState = useDarkTheme, label = "animate Thema",
        transitionSpec = {
            fadeIn(
                tween(1_000, easing = LinearEasing)

            ).togetherWith(
                fadeOut(
                    tween(1000, easing = LinearEasing)
                ) //+ scaleOut(tween(500))

            )
        }) { darkMode ->
        if (!view.isInEditMode) {
            val window = (view.context as Activity).window
            WindowCompat.getInsetsController(window, window.decorView).apply {
                isAppearanceLightStatusBars = !useDarkTheme // Control icon visibility
            }
        }
        val themeColors = if (darkMode)
            extendedDark else extendedLight

        CompositionLocalProvider(
            LocalExtendedColors provides themeColors,
        ) {

            MaterialTheme(
                colorScheme = themeColors.material,
                typography = AppTypography,
                content = content
            )
        }
    }
}
